"""
WordPress Theme Generator using Deep Agents

Usage:
    python app.py                           # Run demo
    python app.py --multi                   # Use multi-agent approach
    python app.py --theme "My Theme" --slug my-theme --author "John Doe"

Or programmatically:
    from agents import generate_theme
    result = generate_theme(
        theme_name="My Theme",
        theme_slug="my-theme",
        author="Developer",
        uploaded_files={"index.html": "...", "style.css": "..."}
    )
"""

