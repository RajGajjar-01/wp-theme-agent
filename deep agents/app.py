    print("=" * 60)

    if use_sub_agents:
        print("\nUsing multi-agent workflow: Analyzer → Generator → ACF → Validator")

    result = generate_theme(
        theme_name=theme_name,
        theme_slug=theme_slug,
        author=author,
        uploaded_files=uploaded_files,
        use_sub_agents=use_sub_agents,
    )

    print("\n" + "=" * 60)
    print(f"Result: {'SUCCESS' if result['ok'] else 'FAILED'}")
    print(f"Theme path: {result['theme_path']}")
